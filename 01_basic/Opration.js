
Postfix 
let x = 3;
const y = x++;

console.log(`x:${x}, y:${y}`);

Prefix
let a = 2;
let b = ++a;
console.log(`a:${a},b:${b}`);

// in prefix a value is increment by +1 and the result of both variable are same
// in postfix a value is increment by +1 so first value of x is same after it increment by 1
// so postfix value x value is increment by +1 and y is same
